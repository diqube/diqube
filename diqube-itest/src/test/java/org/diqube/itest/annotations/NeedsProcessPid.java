package org.diqube.itest.annotations;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Annotation on a test method that needs to find out PIDs of subprocesses created with {@link ProcessBuilder}.
 * 
 * As this functionality is platform dependant, corresponding test methods will be ignored on systems where we cannot
 * identify a process' PID.
 *
 * @author Bastian Gloeckle
 */
@Target(ElementType.METHOD)
@Retention(RetentionPolicy.RUNTIME)
public @interface NeedsProcessPid {

}
